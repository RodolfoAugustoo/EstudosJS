/*
    Operadores artiméticos são operadores binários
    Porque sempre estão entre dois operandos
*/
const [a, b, c, d] = [5, 5, 7, 8]

soma = a + b + c + d
substracao = a - b - c - d
multiplicacao = a * b
divisao = c / d
modulo = a % b

console.log(soma, substracao, multiplicacao, divisao, modulo)